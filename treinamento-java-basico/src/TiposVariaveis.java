public class TiposVariaveis {
    public static void main(String[] args) throws Exception {
       
        int numero = 5;
        numero = 10;
        System.out.println(numero);

        final double VALOR_DE_PI = 3.14; /*Por ser uma constante deve conter a palavra final e ser escrita a variável em maiúscula */        
        System.out.println(VALOR_DE_PI);

        int n = 5; 
        n = - n;  /* Operador unário serve para inverter o sinal */
        System.out.println(n); 

        n = n * -1; /*Para transformar em número positivo */
        System.out.println(n);
        



        

    }
}
